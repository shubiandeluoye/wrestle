    public IPlayerActions Player => m_PlayerActionsCallbackInterface ?? (m_PlayerActionsCallbackInterface = new PlayerActions(this));

    public interface IPlayerActions
    {
        void OnMove(InputAction.CallbackContext context);
        InputAction Move { get; }
    }

    private class PlayerActions : IPlayerActions
    {
        private readonly WJPlayerControls m_Wrapper;

        public PlayerActions(WJPlayerControls wrapper) { m_Wrapper = wrapper; }

        public void OnMove(InputAction.CallbackContext context)
        {
            var callbackHandler = m_Wrapper.m_PlayerActionsCallbackInterface;
            if (callbackHandler != null)
                callbackHandler.OnMove(context);
        }

        public InputAction Move => m_Wrapper.m_Player_Move;
    }
